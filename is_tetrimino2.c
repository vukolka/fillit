int		is_stick(int *a)
{
	if (a[0] == a[1] - 1 &&
		a[1] == a[2] - 1 &&
		a[2] == a[3] - 1)
	{
		return (2);
	}
	if (a[0] == a[1] - 5 &&
		a[1] == a[2] - 5 &&
		a[2] == a[3] - 5)
	{
		return (3);
	}
	return (0);
}

int		is_square(int *a)
{
	if (a[0] == a[1] - 1 &&
		a[1] == a[3] - 5 &&
		a[0] == a[2] - 5)
	{
		return (1);
	}
	return (0);
}

